"""
02 - Operadores numéricos:
    Python proporciona varios operadores para realizar operaciones
    aritméticas:
    - Suma (+): a + b
    - Resta (-): a - b
    - Multiplicación (*): a * b
    - División (/): a / b
    - División entera (//): a // b
    - Módulo (%): a % b
    - Potencia (**): a ** b
"""
print("\n--- Operadores numéricos ---")
# Ejercicio 2: Realiza operaciones aritméticas básicas.
a = 20
b = 5
suma = a + b
resta = a - b
multiplicacion = a * b
division = a / b
division_entera = a // b
modulo = a % b
potencia = a ** b
print(f"{a} + {b} = {suma}")
print(f"{a} - {b} = {resta}")
print(f"{a} * {b} = {multiplicacion}")
print(f"{a} / {b} = {division}")
print(f"{a} // {b} = {division_entera}")
print(f"{a} % {b} = {modulo}")
print(f"{a} ** {b} = {potencia}")