"""
03 - Operadores de comparación:
    Se utilizan para comparar dos valores:
    - Igual (==): x == y
    - Diferente (!=): x != y
    - Mayor que (>): x > y
    - Menor que (<): x < y
    - Mayor o igual que (>=): x >= y
    - Menor o igual que (<=): x <= y
"""
print("\n--- Operadores de comparación ---")
# Ejercicio 3: Compara dos valores.
x = 15
y = 10
igual = x == y
diferente = x != y
mayor_que = x > y
menor_que = x < y
mayor_o_igual_que = x >= y
menor_o_igual_que = x <= y
print(f"{x} == {y}: {igual}")
print(f"{x} != {y}: {diferente}")
print(f"{x} > {y}: {mayor_que}")
print(f"{x} < {y}: {menor_que}")
print(f"{x} >= {y}: {mayor_o_igual_que}")
print(f"{x} <= {y}: {menor_o_igual_que}")